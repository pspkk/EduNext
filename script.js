// --- ส่วนการจัดการหน้า Home (Live Search Preview) ---
function initHomeSearch() {
    const homeSearch = document.getElementById('home-search');
    const previewBox = document.getElementById('search-preview');
    const previewList = document.getElementById('preview-list');

    // เช็คก่อนว่ามี element เหล่านี้ในหน้าปัจจุบันไหม (กัน Error ในหน้า Library)
    if (!homeSearch || !previewBox || !previewList) return;

    homeSearch.addEventListener('input', function() {
        const term = this.value.toLowerCase().trim();
        
        if (term.length < 2) {
            previewBox.classList.add('hidden');
            return;
        }

        // กรองหาแค่ 3 อันแรกมาโชว์พรีวิว
        const matches = books.filter(book => 
            book.title.toLowerCase().includes(term) || 
            book.keywords.some(kw => kw.toLowerCase().includes(term))
        ).slice(0, 3);

        if (matches.length > 0) {
            previewList.innerHTML = matches.map(book => `
                <div onclick="window.location.href='archive.html?search=${encodeURIComponent(book.title)}'" 
                     class="p-4 hover:bg-zinc-50 cursor-pointer border-b border-zinc-50 last:border-0 flex items-center gap-4 group text-left">
                    <img src="${book.img}" class="w-10 h-14 object-cover rounded shadow-sm">
                    <div>
                        <div class="text-sm font-bold text-zinc-800 group-hover:text-[#af101a] line-clamp-1">${book.title}</div>
                        <div class="text-xs text-zinc-500">${book.author}</div>
                    </div>
                </div>
            `).join('');
            previewBox.classList.remove('hidden');
        } else {
            previewList.innerHTML = `<div class="p-4 text-sm text-zinc-400 text-center">No results found.</div>`;
            previewBox.classList.remove('hidden');
        }
    });

    // กด Enter ในหน้า Home ให้ไปหน้า Library
    homeSearch.addEventListener('keypress', (e) => {
        if (e.key === 'Enter') {
            const query = homeSearch.value;
            window.location.href = `archive.html?search=${encodeURIComponent(query)}`;
        }
    });

    // ปิดพรีวิวเมื่อคลิกที่อื่น
    document.addEventListener('click', (e) => {
        if (!homeSearch.contains(e.target) && !previewBox.contains(e.target)) {
            previewBox.classList.add('hidden');
        }
    });
}

