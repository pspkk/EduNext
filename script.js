// 3. ฟังก์ชันกรองข้อมูล (Filter & Search)
function filterData() {
    const searchTerm = document.getElementById('search-input').value.toLowerCase();
    const selectedYear = document.getElementById('year-filter').value;
    const selectedType = document.getElementById('type-filter').value.toLowerCase();
    const selectedCategory = document.getElementById('category-filter').value.toLowerCase();

    filteredBooks = books.filter(book => {
        // 1. Search Logic (Title & Keywords)
        const titleMatch = book.title?.toLowerCase().includes(searchTerm);
        const keywordMatch = book.keywords?.some(kw => kw.toLowerCase().includes(searchTerm)) || false;
        const matchesSearch = titleMatch || keywordMatch;

        // 2. Year Logic
        const matchesYear = selectedYear === "all" || book.year?.toString() === selectedYear;

        // 3. Type Logic
        const matchesType = selectedType === "all" || 
            (book.type?.toLowerCase() === selectedType);

        // 4. Category Logic (รองรับทั้ง String และ Array)
        let matchesCategory = false;
        if (selectedCategory === "all") {
            matchesCategory = true;
        } else if (book.category) {
            if (Array.isArray(book.category)) {
                // ถ้า category เป็น Array ให้เช็คว่ามีตัวไหนตรงกับที่เลือกไหม
                matchesCategory = book.category.some(cat => cat.toLowerCase() === selectedCategory);
            } else {
                // ถ้าเป็น String ปกติ
                matchesCategory = book.category.toLowerCase() === selectedCategory;
            }
        }

        return matchesSearch && matchesYear && matchesType && matchesCategory;
    });

    currentPage = 1; 
    displayContent(); 
}

