document.addEventListener('DOMContentLoaded', () => {
    // 6.1 ผูก Event Listeners ให้กับตัวกรองทุกตัว
    const inputs = ['search-input', 'year-filter', 'type-filter', 'category-filter'];
    
    inputs.forEach(id => {
        const el = document.getElementById(id);
        if (el) {
            // ถ้าเป็นช่องค้นหาใช้ 'input' (พิมพ์ปุ๊บกรองปั๊บ), ถ้าเป็น dropdown ใช้ 'change'
            const eventType = id === 'search-input' ? 'input' : 'change';
            el.addEventListener(eventType, filterData);
        }
    });

    // 6.2 ตรวจสอบคำค้นหาจาก URL (เช่น ?search=การศึกษา)
    const urlParams = new URLSearchParams(window.location.search);
    const searchQuery = urlParams.get('search');

    if (searchQuery) {
        const searchInput = document.getElementById('search-input');
        if (searchInput) {
            // 1. นำคำจาก URL ไปแสดงในช่อง Input
            searchInput.value = decodeURIComponent(searchQuery);
            // 2. รันฟังก์ชันกรองข้อมูลทันที
            filterData();
        }
    } else {
        // ถ้าไม่มีคำค้นหาใน URL ให้แสดงข้อมูลทั้งหมดตามปกติ
        displayContent();
    }
});

